package com.dto;

public class PesiDTO {

	int pesoAttori;
	int pesoRegista;
	int pesoScrittori;
	int pesoGeneri;
	/**
	 * @return the pesoAttori
	 */
	public int getPesoAttori() {
		return pesoAttori;
	}
	/**
	 * @param pesoAttori the pesoAttori to set
	 */
	public void setPesoAttori(int pesoAttori) {
		this.pesoAttori = pesoAttori;
	}
	/**
	 * @return the pesoRegista
	 */
	public int getPesoRegista() {
		return pesoRegista;
	}
	/**
	 * @param pesoRegista the pesoRegista to set
	 */
	public void setPesoRegista(int pesoRegista) {
		this.pesoRegista = pesoRegista;
	}
	/**
	 * @return the pesoScrittori
	 */
	public int getPesoScrittori() {
		return pesoScrittori;
	}
	/**
	 * @param pesoScrittori the pesoScrittori to set
	 */
	public void setPesoScrittori(int pesoScrittori) {
		this.pesoScrittori = pesoScrittori;
	}
	/**
	 * @return the pesoGeneri
	 */
	public int getPesoGeneri() {
		return pesoGeneri;
	}
	/**
	 * @param pesoGeneri the pesoGeneri to set
	 */
	public void setPesoGeneri(int pesoGeneri) {
		this.pesoGeneri = pesoGeneri;
	}
	
	
}
